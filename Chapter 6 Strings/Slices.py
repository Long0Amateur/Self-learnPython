# Slices 

s = 'abcdefghij'
# index =  0123456789

print(s)
print(sep = '')

print('Characters at indices 2,3,4 =',s[2:5])   
 
print('First five characters =',s[:5])   

print('Characters from index 5 to the end =',s[5:])    

print('Last two characters =',s[-2:])   

print('Entire string =',s[ : ])   

print('characters from index 1 to 6, by twos =',s[1:7:2])  

print('A negative step reverses the string =',s[::-1])   
      
